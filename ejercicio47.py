# listas o arrays

frutas=["manzana","banana","tomate"]
print(frutas)

for fruta in frutas:
    print(fruta)

# en los arrays se pueden combinar 
numeros=[1,2,'3',4.1,"dato"]
print(numeros)
for numero in numeros:
    print(numero)

# SUBDATOS en los arrays 
subdatos=[6,[5,6,7],7,8]
print(subdatos)
for subdato in subdatos:
    print(subdato)

