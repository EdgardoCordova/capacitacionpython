"""
variables de tipo booleana
solo pueden tener un TRUE o FALSE

"""
a=True
b=False

# operadores LOGICOS and o &,  or o | 
print( a and b)     # resultado     FALSE
print( a or b)      # resultado     TRUE
print( a & b)       # resultado     FALSE
print( a | b)       # resultado     TRUE
print( not a )      # resultado     FALSE

